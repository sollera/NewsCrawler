package com.crawl.dto;

public class statusVO {
	private int chosun;
	private int donga;
	private int seoul;
	private int ytn;
	private int segye;
	private int newdaily;
	private String modifyTime;
	
	public int getChosun() {
		return chosun;
	}
	public void setChosun(int chosun) {
		this.chosun = chosun;
	}
	public int getDonga() {
		return donga;
	}
	public void setDonga(int donga) {
		this.donga = donga;
	}
	public int getSeoul() {
		return seoul;
	}
	public void setSeoul(int seoul) {
		this.seoul = seoul;
	}
	public int getYtn() {
		return ytn;
	}
	public void setYtn(int ytn) {
		this.ytn = ytn;
	}
	public int getSegye() {
		return segye;
	}
	public void setSegye(int segye) {
		this.segye = segye;
	}
	public int getNewdaily() {
		return newdaily;
	}
	public void setNewdaily(int newdaily) {
		this.newdaily = newdaily;
	}
	public String getModifyTime() {
		return modifyTime;
	}
	public void setModifyTime(String modifyTime) {
		this.modifyTime = modifyTime;
	}
}
